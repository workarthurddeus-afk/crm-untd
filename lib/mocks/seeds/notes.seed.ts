import type { Note, NoteEffort, NoteImpact, NotePriority, NoteStatus, NoteTypeValue } from '@/lib/types'
import { isoDaysAgo } from '@/lib/utils/date'
import { generateExcerpt, normalizeTag } from '@/lib/utils/notes'

interface SeedNoteInput {
  id: string
  title: string
  content: string
  type: NoteTypeValue
  status: NoteStatus
  priority: NotePriority
  impact: NoteImpact
  effort: NoteEffort
  color: Note['color']
  tags: string[]
  folderId?: string | null
  isPinned?: boolean
  isFavorite?: boolean
  isArchived?: boolean
  relatedLeadId?: string | null
  relatedTaskId?: string | null
  relatedFeedbackId?: string | null
  relatedProjectId?: string | null
  source?: Note['source']
  createdAt: string
  updatedAt: string
  lastViewedAt?: string | null
}

function note(input: SeedNoteInput): Note {
  const tags = input.tags.map(normalizeTag)
  const isPinned = input.isPinned ?? false
  const isFavorite = input.isFavorite ?? false
  const isArchived = input.isArchived ?? false
  return {
    ...input,
    excerpt: generateExcerpt(input.content, 180),
    tags,
    folderId: input.folderId ?? null,
    isPinned,
    isFavorite,
    isArchived,
    isDeleted: false,
    relatedLeadId: input.relatedLeadId ?? null,
    relatedTaskId: input.relatedTaskId ?? null,
    relatedFeedbackId: input.relatedFeedbackId ?? null,
    relatedProjectId: input.relatedProjectId ?? null,
    source: input.source ?? 'manual',
    lastViewedAt: input.lastViewedAt ?? null,
    tagIds: tags,
    relatedTo: input.relatedLeadId ? 'lead' : input.relatedTaskId ? 'task' : input.relatedFeedbackId ? 'feedback' : 'general',
    pinned: isPinned,
    favorited: isFavorite,
    expectedImpact: input.impact,
    estimatedEffort: input.effort,
  }
}

