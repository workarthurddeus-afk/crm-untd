import type { Feedback, FeedbackInput } from '@/lib/types'
import { isoDaysAgo, nowIso } from '@/lib/utils/date'

const now = nowIso()

function feedback(
  id: string,
  daysAgo: number,
  input: FeedbackInput,
  resolvedAt: string | null = null
): Feedback {
  return {
    id,
    ...input,
    createdAt: isoDaysAgo(daysAgo),
    updatedAt: now,
    resolvedAt,
  }
}

